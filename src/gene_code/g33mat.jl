function cal_g33mat(g012,g013,g023,g031,g032,g033)
[((2.0*g023*(2.0*g012*g031 + g032) + 2.0*g013*(g031 - 2.0*g012*g032) + (1.0 + 4.0*Power(g012,2.0))*g033)/4.0)  ((-2.0*g023*(2.0*g012*g031 + g032) + 2.0*g013*(g031 + 2.0*g012*g032) + (1.0 - 4.0*Power(g012,2.0))*g033)/4.0)  ;
((2.0*g023*(-2.0*g012*g031 + g032) - 2.0*g013*(g031 - 2.0*g012*g032) + (1.0 - 4.0*Power(g012,2.0))*g033)/4.0)  ((-2.0*(g013 - 2.0*g012*g023)*g031 - 2.0*(2.0*g012*g013 + g023)*g032 + (1.0 + 4.0*Power(g012,2.0))*g033)/4.0)  ;
]
end
